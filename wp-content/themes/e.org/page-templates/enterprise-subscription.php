<script type="text/javascript">
    $(function(){
        function alignCols() {
            $('.align-row').each(function() {
                var height = 0;
                $(this).find('.align-col').each(function() {
                    if ($(this).height() > height) {
                        height = $(this).height();
                    }
                });
                $(this).find('.align-col').height(height);
            });
        }

        $(window).on("load", function($) {
            alignCols();
        });

    });
</script>
